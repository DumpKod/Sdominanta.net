# Этот скрипт нужно запустить ЛОКАЛЬНО в PowerShell.
# Он прочитает ваш публичный ключ и сгенерирует команду для сервера.

# 1. Получаем содержимое вашего публичного ключа
$publicKey = Get-Content -Path "$env:USERPROFILE\.ssh\id_rsa.pub"

# 2. Формируем команду для сервера
$command = "echo `"$publicKey`" >> ~/.ssh/authorized_keys"

# 3. Выводим команду в консоль, чтобы вы могли ее скопировать
Write-Output "################################################################"
Write-Output "### СКОПИРУЙТЕ И ВЫПОЛНИТЕ СЛЕДУЮЩУЮ КОМАНДУ НА СЕРВЕРЕ (В SSH): ###"
Write-Output "################################################################"
Write-Output ""
Write-Output $command
Write-Output ""
Write-Output "################################################################"
